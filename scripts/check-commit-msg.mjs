#!/usr/bin/env node
// Valida el mensaje de commit antes de que git lo acepte (hook commit-msg, vía lefthook).
// Dos reglas, en este orden:
//   1) ATRIBUCIÓN DE IA PROHIBIDA: el proyecto NO permite líneas de coautoría/atribución
//      generadas por herramientas de IA (regla dura — ver CLAUDE.md).
//   2) CONVENTIONAL COMMITS: el asunto debe seguir `tipo(scope opcional): descripción`.
//
// Uso: node scripts/check-commit-msg.mjs <ruta-al-archivo-del-mensaje>
// lefthook pasa {1} = ruta a .git/COMMIT_EDITMSG. Sale 1 (y aborta el commit) si algo no valida.

import { readFileSync } from "node:fs";

/** Tipos permitidos por conventional commits (alineados con CLAUDE.md: feat/fix/docs/chore...). */
const TYPES = [
  "feat",
  "fix",
  "docs",
  "chore",
  "refactor",
  "test",
  "style",
  "perf",
  "build",
  "ci",
  "revert",
];

/** Asunto válido: `tipo` + scope opcional `(...)` + `!` opcional (breaking) + `: ` + descripción no vacía. */
const SUBJECT_RE = new RegExp(`^(${TYPES.join("|")})(\\([^)]+\\))?!?: .+`);

/**
 * Líneas/firmas de atribución de IA que NO deben aparecer en ningún commit.
 * Incluye Co-Authored-By (prohibido de plano por la regla) y firmas típicas de herramientas.
 */
const AI_ATTRIBUTION_PATTERNS = [
  { re: /co-authored-by:/i, label: "Co-Authored-By" },
  { re: /generated with \[?claude/i, label: "“Generated with Claude”" },
  { re: /🤖\s*generated with/i, label: "firma 🤖 Generated with" },
  { re: /noreply@anthropic\.com/i, label: "email noreply@anthropic.com" },
  { re: /\bclaude code\b/i, label: "mención “Claude Code”" },
];

/** El hook corre también en merges/reverts autogenerados: ahí se saltea la regla de formato. */
const AUTOGENERATED_PREFIXES = [/^merge /i, /^revert /i, /^fixup! /i, /^squash! /i];

function fail(message) {
  console.error(`\n✖ commit-msg rechazado\n${message}\n`);
  process.exit(1);
}

const msgPath = process.argv[2];
if (!msgPath) {
  fail("Uso interno: falta la ruta al archivo del mensaje de commit.");
}

let raw;
try {
  raw = readFileSync(msgPath, "utf8");
} catch (error) {
  fail(`No se pudo leer el mensaje de commit en "${msgPath}": ${error.message}`);
}

// Líneas reales del mensaje: se descartan comentarios (#) y el diff de `git commit -v`.
const lines = raw
  .split(/\r?\n/)
  .filter((line) => !line.startsWith("#"))
  .filter((line) => line.trim() !== "diff --git" && !line.startsWith("diff --git"));

const fullMessage = lines.join("\n");
const subject = lines.find((line) => line.trim() !== "")?.trim() ?? "";

// git aborta solo si el mensaje queda vacío; no es trabajo de este hook.
if (subject === "") {
  process.exit(0);
}

// 1) Atribución de IA — se chequea SIEMPRE, incluso en merges/reverts.
for (const { re, label } of AI_ATTRIBUTION_PATTERNS) {
  if (re.test(fullMessage)) {
    fail(
      `El mensaje contiene atribución de IA prohibida: ${label}.\n` +
        "Regla del proyecto: nunca agregar Co-Authored-By ni firmas de herramientas de IA.\n" +
        "Quitá esa línea del mensaje y volvé a commitear.",
    );
  }
}

// 2) Conventional commits — se saltea en mensajes autogenerados (merge/revert/fixup/squash).
const isAutogenerated = AUTOGENERATED_PREFIXES.some((re) => re.test(subject));
if (!isAutogenerated && !SUBJECT_RE.test(subject)) {
  fail(
    `El asunto no sigue Conventional Commits:\n  "${subject}"\n\n` +
      `Formato: <tipo>(scope opcional): descripción\n` +
      `Tipos válidos: ${TYPES.join(", ")}\n` +
      `Ejemplos:\n` +
      `  feat(admin): validar el shape de la config antes de persistir\n` +
      `  fix(mc-status): degradar a offline cuando mcstatus.io falla\n` +
      `  docs: actualizar la guía de metodología`,
  );
}

process.exit(0);
